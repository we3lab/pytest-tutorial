""" This module contains some functions that perform basic arithmetic operations. """

def add(a, b):
    """ This function adds two numbers. """
    return a + b

def subtract(a, b):
    """ This function subtracts two numbers. """
    return a - b

def multiply(a, b):
    """ This function multiplies two numbers. """
    return a * b

def divide(a, b):
    """ This function divides two numbers. """
    return a / b
